    private void changePWfunction() {
        if (this.user_currentPW_proPage.getText().trim().isEmpty() || this.user_newPW_proPage.getText().trim().isEmpty() || this.user_newPWReEnter_proPage.getText().trim().isEmpty()) {
            this.changepw_errorMSG.setForeground(new Color(255, 0, 0));
            changepw_errorMSG.setText("Empty Field");
        } else if (!this.user_currentPW_proPage.getText().equals(this.userPASSWORD_DB_Homepage)) {
            this.changepw_errorMSG.setForeground(new Color(255, 0, 0));
            changepw_errorMSG.setText("Entered Password Is Wrong");
        } else if (!this.user_newPW_proPage.getText().equals(this.user_newPWReEnter_proPage.getText())) {
            this.changepw_errorMSG.setForeground(new Color(255, 0, 0));
            changepw_errorMSG.setText("New Passwords Does Not Match");
        } else {
            String newPassword = this.user_newPW_proPage.getText();
            try {
                int result = ChangeUserPWMODEL.changePW(this.USERID_HomePage, newPassword);
                if (result == 0) {
                    this.changepw_errorMSG.setForeground(new Color(0, 204, 0));
                    this.changepw_errorMSG.setText("...Password Update Successful...");
                } else {
                    this.changepw_errorMSG.setText("ERROR 2");
                }
            } catch (Exception e) {
                System.err.println("Got an exception! ");
                System.err.println(e.getMessage());
            }
        }
    }

